  <h2>Ingredients:</h2>
  <ul>
    <li>1/4 cup vegetable oil</li>
    <li>12 corn tortillas</li>
    <li>3 cups shredded cheddar cheese</li>
    <li>1/4 cup chopped onion</li>
    <li>2 cups enchilada sauce</li>
    <li>Sour cream, cilantro, and sliced olives for garnish (optional)</li>
  </ul>
  <h2>Directions:</h2>
  <ol>
    <li>Preheat oven to 375Â°F and lightly grease a 9x13 inch baking dish.</li>
    <li>
      In a small skillet over medium-high heat, heat oil until shimmering. Fry each tortilla for a
      few seconds on both sides until soft and pliable. Drain on paper towels and set aside.
    </li>
    <li>In a small bowl, toss cheese and onion together.</li>
    <li>
      Spoon about 2 tablespoons of cheese mixture onto each tortilla and roll up tightly. Place
      seam-side down in the prepared baking dish. Repeat with remaining tortillas and cheese.
    </li>
    <li>Pour enchilada sauce evenly over the tortillas, making sure to cover them completely.</li>
    <li>Bake for 15 to 20 minutes or until cheese is melted and bubbly.</li>
    <li>Serve hot with sour cream, cilantro, and olives if desired.</li>
  </ol>

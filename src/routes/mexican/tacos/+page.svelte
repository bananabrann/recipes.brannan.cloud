<p>
  Classic Beef Tacos are a beloved Mexican dish that combines seasoned ground beef with a 
  variety of toppings, all wrapped in soft or crispy taco shells. This recipe yields flavorful and 
  satisfying tacos that are perfect for a casual weeknight dinner or a festive gathering.
</p>

<h2>Ingredients</h2>
<ul>
  <li>1 lb (450g) ground beef</li>
  <li>1 small onion, finely chopped</li>
  <li>2 cloves garlic, minced</li>
  <li>1 packet (about 2 tablespoons) taco seasoning mix</li>
  <li>1/2 teaspoon cumin powder</li>
  <li>1/4 teaspoon chili powder (adjust to taste)</li>
  <li>Salt and pepper to taste</li>
  <li>1 tablespoon vegetable oil</li>
</ul>

<h2>Instructions</h2>
<ol>
  <li>Heat the vegetable oil in a large skillet over medium-high heat.</li>
  <li>Heat the vegetable oil in a large skillet over medium-high heat.</li>
  <li>Add the minced garlic and cook for about 30 seconds until fragrant.</li>
  <li>Add the ground beef to the skillet and break it up with a spatula. Cook until browned and fully cooked, stirring occasionally.</li>
  <li>Drain excess fat from the skillet if necessary.</li>
  <li>Sprinkle the taco seasoning, cumin, chili powder, salt, and pepper over the cooked beef. Mix well to ensure even distribution of the seasonings. You can also add a little water if the mixture seems dry.</li>
  <li>Simmer the seasoned beef for a few more minutes, allowing the flavors to meld together. Remove from heat and set aside.</li>
  <li>Prepare your taco shells according to the package instructions. If using hard shells, you might want to warm them up in the oven before assembling.</li>
  <li>To assemble, spoon a generous amount of the seasoned beef mixture into each taco shell.</li>
  <li>Top with shredded lettuce, diced tomatoes, shredded cheddar cheese, sour cream, sliced jalapenos (if using), and chopped cilantro</li>
  <li>Squeeze a wedge of lime over each taco to add a burst of citrusy flavor.</li>
  <li>Serve the tacos immediately while the shells are still crisp or soft, depending on your preference.</li>
</ol>

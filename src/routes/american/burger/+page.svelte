<h2>Ingredients</h2>
<ul>
  <li>1 pound ground beef</li>
  <li>1/2 teaspoon salt</li>
  <li>1/2 teaspoon pepper</li>
  <li>4 slices cheese</li>
  <li>4 hamburger buns</li>
  <li>4 tablespoons butter</li>
  <li>4 tablespoons mayonnaise</li>
  <li>4 tablespoons ketchup</li>
</ul>

<h2>Instructions</h2>
<ol>
  <li>Preheat grill to medium-high heat.</li>
  <li>Divide ground beef into 4 equal portions and form into patties.</li>
  <li>Season both sides of each patty with salt and pepper.</li>
  <li>Grill burgers for 4-5 minutes per side, or until desired doneness.</li>
  <li>Top each burger with a slice of cheese during the last minute of cooking.</li>
  <li>Spread butter on cut sides of hamburger buns and toast on the grill.</li>
  <li>Spread mayonnaise and ketchup on toasted buns and top with burgers.</li>
</ol>


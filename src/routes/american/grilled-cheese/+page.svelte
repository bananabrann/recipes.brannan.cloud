<h1>Grilled Cheese</h1>

<img src="https://placehold.co/400x280" alt="A delicious grilled cheese." />



<h2>Ingredients</h2>
<ul>
  <li>4 slices of white or whole wheat bread</li>
  <li>4 slices of American cheese (or cheddar, if preferred)</li>
  <li>2 tablespoons of butter, softened</li>
  <li>Optional: sliced tomatoes, cooked bacon, or pickles for added flavor</li>
</ul>

<h2>Instructions</h2>
<ol>
  <li>Preheat a skillet or griddle over medium heat.</li>
  <li>Spread butter on one side of each slice of bread.</li>
  <li>
    Place one slice of cheese (and any optional ingredients, if desired) between two slices of bread
    with the buttered side out.
  </li>
  <li>
    Place the sandwich on the skillet, and cook until the bread is golden brown and the cheese is
    melted, about 2-3 minutes per side.
  </li>
  <li>Repeat with the remaining ingredients.</li>
  <li>Remove from the skillet and let it cool for a minute. Slice in half, and serve warm.</li>
</ol>

<p>
  Enjoy your classic American grilled cheese sandwich! Pair with tomato soup for an even more
  quintessential American experience.
</p>